// Activity 1: Arithmetic Operations

// Task 1: Add two numbers and log the result to the console.
let num1 = 10;
let num2 = 5;
let sum = num1 + num2;
console.log(sum); // Output: 15

// Task 2: Subtract two numbers and log the result to the console.
let difference = num1 - num2;
console.log(difference); // Output: 5

// Task 3: Multiply two numbers and log the result to the console.
let product = num1 * num2;
console.log(product); // Output: 50

// Task 4: Divide two numbers and log the result to the console.
let quotient = num1 / num2;
console.log(quotient); // Output: 2

// Task 5: Find the remainder when one number is divided by another and log the result to the console.
let remainder = num1 % num2;
console.log(remainder); // Output: 0


// Activity 2: Assignment Operators

// Task 6: Add a number to a variable and log the result to the console.
let variable = 20;
variable += 10; // Equivalent to variable = variable + 10
console.log(variable); // Output: 30

// Task 7: Subtract a number from a variable and log the result to the console.
variable -= 5; // Equivalent to variable = variable - 5
console.log(variable); // Output: 25


// Activity 3: Comparison Operators

// Task 8: Compare two numbers using > and log the result to the console.
let isGreater = num1 > num2;
console.log(isGreater); // Output: true

// Task 9: Compare two numbers using < and log the result to the console.
let isLess = num1 < num2;
console.log(isLess); // Output: false

// Task 10: Compare two numbers using === and log the result to the console.
let isEqual = num1 === num2;
console.log(isEqual); // Output: false


// Activity 4: Logical Operators

// Task 11: Use the && operator to combine two conditions and log the result to the console.
let condition1 = num1 > 0;
let condition2 = num2 < 10;
let andResult = condition1 && condition2;
console.log(andResult); // Output: true

// Task 12: Use the || operator to combine two conditions and log the result to the console.
let orResult = condition1 || condition2;
console.log(orResult); // Output: true

// Task 13: Use the ! operator to negate a condition and log the result to the console.
let notCondition = !condition1;
console.log(notCondition); // Output: false


// Activity 5: Ternary Operator

// Task 14: Use the ternary operator to check if a number is positive or negative and log the result to the console.
let number = -3;
let result = number >= 0 ? "Positive" : "Negative";
console.log(result); // Output: Negative
